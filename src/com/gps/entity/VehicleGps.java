package com.gps.entity;

public class VehicleGps {

	private Integer gpsId;
	private String gpsCode;
	private String gpsTime;
	private Double gpsLng;
	private Double gpsLat;
	private String gpsDiming;
	public Integer getGpsId() {
		return gpsId;
	}
	public void setGpsId(Integer gpsId) {
		this.gpsId = gpsId;
	}
	public String getGpsCode() {
		return gpsCode;
	}
	public void setGpsCode(String gpsCode) {
		this.gpsCode = gpsCode;
	}
	public String getGpsTime() {
		return gpsTime;
	}
	public void setGpsTime(String gpsTime) {
		this.gpsTime = gpsTime;
	}
	public Double getGpsLng() {
		return gpsLng;
	}
	public void setGpsLng(Double gpsLng) {
		this.gpsLng = gpsLng;
	}
	public Double getGpsLat() {
		return gpsLat;
	}
	public void setGpsLat(Double gpsLat) {
		this.gpsLat = gpsLat;
	}
	public String getGpsDiming() {
		return gpsDiming;
	}
	public void setGpsDiming(String gpsDiming) {
		this.gpsDiming = gpsDiming;
	}
}
